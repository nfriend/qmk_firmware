bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (!process_record_dynamic_macro(keycode, record)) {
    return false;
  }
  switch (keycode) {
    case EPRM:
      if (record->event.pressed) {
        eeconfig_init();
      }
      return false;
      break;
    case RGB_SLD:
      if (record->event.pressed) {
        rgblight_mode(1);
      }
      return false;
      break;
    case HSV_0_255_255:
      if (record->event.pressed) {
        #ifdef RGBLIGHT_ENABLE
          rgblight_enable();
          rgblight_mode(1);
          rgblight_sethsv(0,255,255);
        #endif
      }
      return false;
      break;
    case HSV_120_255_128:
      if (record->event.pressed) {
        #ifdef RGBLIGHT_ENABLE
          rgblight_enable();
          rgblight_mode(1);
          rgblight_sethsv(120,255,128);
        #endif
      }
      return false;
      break;
    case HSV_240_255_255:
      if (record->event.pressed) {
        #ifdef RGBLIGHT_ENABLE
          rgblight_enable();
          rgblight_mode(1);
          rgblight_sethsv(240,255,255);
        #endif
      }
      return false;
      break;
    case MAC_MODE:
      if (record->event.pressed) {
        layer_on(MAC);
        layer_off(WINDOWS);
      }
      return false;
      break;
    case WINDOWS_MODE:
      if (record->event.pressed) {
        layer_on(WINDOWS);
        layer_off(MAC);
      }
      return false;
      break;
  }
  return true;
}

uint32_t layer_state_set_user(uint32_t state) {

    uint8_t layer = biton32(state);

    ergodox_board_led_off();
    ergodox_right_led_1_off();
    ergodox_right_led_2_off();
    ergodox_right_led_3_off();

    if (layer == ARROWS_MAC || layer == ARROWS_WINDOWS) {
      ergodox_right_led_1_on();
    } else if (layer == SYMBOLS) {
      ergodox_right_led_2_on();
    } else if (layer == NUMPAD) {
      ergodox_right_led_1_on();
      ergodox_right_led_2_on();
    } else if (layer == WM_MAC || layer == WM_WINDOWS) {
      ergodox_right_led_2_on();
      ergodox_right_led_3_on();
    } else if (layer == MISC) {
      ergodox_right_led_1_on();
      ergodox_right_led_2_on();
      ergodox_right_led_3_on();
    } else if (layer == WINDOWS) {
      ergodox_right_led_3_on();
    } else if (layer == MAC) {
      // do nothing
    }

    return state;
};

void keyboard_post_init_user(void) {

  // This layer is always on
  layer_on(COMMON);

  // Default to Mac layout
  layer_on(MAC);
}